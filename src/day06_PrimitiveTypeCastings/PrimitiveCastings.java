package day06_PrimitiveTypeCastings;

public class PrimitiveCastings {

    public static void main(String[] args) {
        byte a=111;
        short b=a;

        int c= (int)b;


        double x=2.99;
        int y= (int) x;

        System.out.println(x);
        System.out.println(y);

    }
}
