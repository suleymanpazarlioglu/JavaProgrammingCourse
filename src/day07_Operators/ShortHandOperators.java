package day07_Operators;

public class ShortHandOperators {
    public static void main(String[] args) {

        int x=10;
        int y=2;

        x*=y;
        System.out.println("x = " + x);

        x=10;
        y=2;

        x+=y;
        System.out.println("x = " + x);

        x=10;
        y=2;

        x-=y;
        System.out.println("x = " + x);

        x=10;
        y=2;

        x/=y;
        System.out.println("x = " + x);

        String word="123"+4;
        System.out.println("word = " + word);

        x=10;
        y=2;

        x%=y;
        System.out.println("x = " + x);











    }
}
